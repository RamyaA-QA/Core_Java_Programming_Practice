package day5_Control_Statements;

public class _2doWhileLoop {

	public static void main(String[] args) {
		
		// Print 10 to 1 numbers
		
				int z=10;
				
				do
					
				{
					System.out.println(z);
					z--;
				}
				
				while(z>0);
				
				
		// Print 1 to 10 numbers
				
				int y=1;
				
				do
					
				{
					System.out.println(y);
					y++;
				}
				
				while(y<=10);

	}

}
